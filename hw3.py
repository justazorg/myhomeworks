a = int(input("Введите первое число:"))
b = int(input("Введите второе число:"))
c = int(input("Введите третье число:"))
if a * b == c:
    print("Умножение удалось")
else:
    print("умножение не удалось")
if a / b == c:
    print("Деление удалось")
else:
    print("Деление не удалось")    

